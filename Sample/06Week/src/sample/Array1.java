package sample;

public class Array1 {
	public static void main(String[] args) {
		int[] a1;
		a1 = new int[3];
		System.out.println(a1[0] + "\t" + a1[1] + "\t" + a1[2]);
		int[] a2 = new int[] {10, 20, 30};
		System.out.println(a2[0] + "\t" + a2[1] + "\t" + a2[2]);
		int[] a3 = {40, 50, 60};
		System.out.println(a3[0] + "\t" + a3[1] + "\t" + a3[2]);
	}
}